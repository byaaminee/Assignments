package assignment3_Yaaminee;

public class Calculations_Part2 {
	
	int sum (int x, int y) {
		int tot = 0;
		tot =  x + y;
		return tot;
	}

	int sum (int x, int y, int z) {
		int tot = 0;
		tot =  x + y + z;
		return tot;
	}

	
	double sum (double x, double y) {
		double tot = 0;
		tot =  x + y;
		return tot;
	}

	
	double sum (double x, double y, double z) {
		double tot = 0;
		tot =  x + y + z;
		return tot;
	}
	
}
